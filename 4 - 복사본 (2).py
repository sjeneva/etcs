def parse_data(data):
    keywords = {}
    lines = data.split('\n')
    for line in lines:
        if '(' in line and line.count('(') == 1 and ')' in line:
            parts = line.split('(')
            word = parts[0].strip()
            weight = parts[1].replace(')', '').strip()
            keywords[word] = float(weight)
    print("Parsed keywords:", keywords)  # Add this line for debugging
    return keywords

def generate_word_cloud(keywords):
    wordcloud = WordCloud(width=800, height=400, background_color='white').generate_from_frequencies(keywords)
    return wordcloud

